a = float (input("Enter the first number:"))
b = float (input("Enter the second number:"))
c = float (input("Enter the third number:"))
numbers = [a, b, c]
numbers.sort()
print("The numbers in ascending order are:", numbers)
# This code takes three numbers as input from the user and sorts them in ascending order.
# It uses the built-in sort method of the list to arrange the numbers in increasing order.

# Output example:
# Enter the first number: 3
# Enter the second number: 1
# Enter the third number: 2
# The numbers in ascending order are: [1.0, 2.0, 3.0]

